public class MoverCell {
    
    private int start;
    private int end;
    
    public MoverCell(int i,int j) {
        this.start = i;
        this.end=j;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }

    
        
}
